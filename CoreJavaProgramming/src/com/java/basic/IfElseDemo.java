package com.java.basic;

public class IfElseDemo {

	public static void main(String[] args) {
		/*
		 *  < - less than
		 *  > - greater than
		 *  == - equal 
		 *  
		 *  Always the output of a conditional operator is TRUE or FALSE
		 *  
		 *  && - Logical AND
		 *  || - Logical OR
		 */
		
		//int a,b;
		int a=20, b=20;
		/*
		 * boolean output = a<b; System.out.println("OUPUT: "+output);
		 */
		if(a<b)
		{
			// IF BLOCK
			System.out.println("A LESS THAN B");
		}
		else {
			// ELSE BLOCK
			System.out.println("A IS NOT LESS THAN B");
		}
		
		
	}

}

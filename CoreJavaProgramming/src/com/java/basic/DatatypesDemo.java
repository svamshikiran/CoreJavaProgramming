package com.java.basic;

public class DatatypesDemo {

	public static void main(String[] args) {
		
		final int CONSTANT = 10;
		
		int integerVariable = 1000; // -2E16 to +2E16
		
		long longVariable = 10000; // -2E64 to +2E64
		
		boolean booleanVariable = true;  //true or false
		
		double doubleVariable = 2.5;
		
		String strVariable = "NAME";
		
		char charVariable = '_';
		
		int[] intArray = {1,2,3,4,5};
		
		char[] charArray = {'N','A','M','E'};
		
		System.out.println("FIRST ELEMENT: "+intArray[0]);
		System.out.println("SECOND ELEMENT: "+intArray[1]);
		System.out.println("THIRD ELEMENT: "+intArray[2]);
		System.out.println("FOURTH ELEMENT: "+intArray[3]);
		System.out.println("FIFTH ELEMENT: "+intArray[4]);
		
		System.out.println("FIRST ELEMENT: "+charArray[0]);
		System.out.println("SECOND ELEMENT: "+charArray[1]);
		System.out.println("THIRD ELEMENT: "+charArray[2]);
		System.out.println("FOURTH ELEMENT: "+charArray[3]);
		
	
		
	}

}
